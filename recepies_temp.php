Французская кухня, изделия из заварного теста с многогранной начинкой. <br>
<span>Ингредиенты </span><br>
•   Пшеничная мука 1 стакан<br>
•   Сливочное масло 100 г<br>
•   Яйцо куриное 3 штуки<br>
•   Соль щепотка<br>
•   Вода 1 стакан<br>
<span>Рецепт </span><br>
- Воду вскипятить. Добавить нарезанное кубиками масло и соль.<br>
- На минимальном огне, просеять муку и помешивать до однородной консистенции.<br>
- Снимаем тесто с огня. Вбиваем яйца и перемешиваем. Масса должна быть однородной.<br>
- На пергамент отсадить шарики из теста с помощью двух ложок или кондитерского мешка.<br>
- Выпекать 20-25 минут<br>
<span>Советы </span><br>
- Не открывайте духовку, иначе ваши профитроли не поднимутся.<br>
- Начинку можете выбрать любую, если это сладкие профитроли, используйте в рецепте столовую ложку сахара, вместо соли. <br>


Кокосовое масло

