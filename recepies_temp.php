<p>Большое изумление и любовь вызывают картинки этих до боли милых котят, хотелось бы их тискать днями и ночами. Но, вы уверены, что это так просто и вы готовы обеспечить райскую жизнь коту? Коты, на самом деле существа с характером и если им что-то не нравиться, они вам это покажут: разорванные шторы, перевернутые горшки с цветами, неожиданная лужа на полу.</p>
<p>•   И так, котикам нужно много любви и внимания, вы это уже поняли? Хорошо. С ними нужно играть, ходить на прогулки, если есть такова возможность. Чем больше вы любите их, тем более ласковы они с вами.</p>
<p>•   Вы уверены, что работа вам позволит ухаживать за ней? Или кошечка будет целый день голодной и одинокой.</p>
<p>•   Если вы заведете котенка, вам придется учить его ходить в туалет, на это уходит много времени. Обучать, чтобы не царапал обои и т.д. Или же вы можете взять взрослого котика, который уже все знает, но, пропустите самый милый период их жизни.</p>
<p>•   Вы готовы покупать наполнитель для лотка и чистить его по несколько раз на день? Так вот, будьте готовы.</p>
<p>•   Будете ли вы проводить кастрацию, которая стоит не мало, или будете разводить потомство? Не забывайте, что коты помечают территорию. В мартовский период, они особо буйные.</p>
<p>•   Кстати, для профилактики паразитов им нужно несколько  раз в год давать таблетки от глистов. Кошечек нужно купать, чистить им лапки и ушки.</p>
<p>•   Если вы уедете в отпуск, должны оставить своего питомца на кого-то ответственного, или брать с собой.</p>
<p>•   Убедитесь, что в доме никто не страдает аллергией.</p>
<p>•   Вы уверены, что вам хватит терпения и ласки? Не берите их ради того, чтобы сгонять злость.</p>
<p>•   Шерсть, она может быть повсюду, это очень важный момент.</p>
<p>Плюсы того что у вас котик:</p>
<p>•   Снимают стресс и раздражительность, для этого достаточно их погладить.</p>
<p>•   Запуская в вас коготки проводят что-то на подобие рефлексотерапии (иглоукалывания). Кошки очень часто ложатся именно на больное место</p>
<p>•   Некоторые исследования подтверждают, что их мурлыканье лечебное.</p>
<p>•   Если у вас есть ребенок, тогда котик станет для него хорошим другом.</p>
<p>•   Ученые утверждают, что кошатники живут на 10 лет дольше.</p>
<p>•   Людям с сердечными и нервными заболеваниями обязательно нужно иметь кошечку.</p>
<p>•   Если вы страдаете от бессонницы, тогда тащите этого пушистика к себе в кровать. </p>
<p>•   Говорят, что засыпая с кошками, мы избавляем свой организм от негатива.</p>

<p>В народе говорят:</p>
<p>Чёрных кошек не стоит бояться, ведь они  забирают всю негативную энергию с человека и его дома;</p>
<p>Серые кошки действуют на наш организм как боярышник и валерьянка;</p>
<p>Рыжие кошки это заряд положительной энергии; </p>
<p>Белые кошки универсальные и лечат все то, что их друзья.</p>
<p>В любом случае, эта мурлыкающая грелочка создаст уют и добавит тепла в ваш дом, так что, решайте сами.</p>
